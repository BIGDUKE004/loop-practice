//  multiples of 3 are numbers that can be divided by 3
// use a loop to print out numbers from 1 to 50
// now place a checker that it should print any number divided by 3 out 

public class Multiples{
        public static void main(String[] args){

        for(int numbers = 1; numbers <= 50; numbers++){
            if(numbers % 3 == 0){
                System.out.println(numbers);            
}
}

}
}
